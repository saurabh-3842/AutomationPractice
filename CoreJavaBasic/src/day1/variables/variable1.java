package day1.variables;

public class variable1 {

	public static void main(String[] args) {
		System.out.println("Program Starts from here");

		int num1 = 452;
		float num2 = 326f;
		double result = num1+num2;

		System.out.println("First Number :            \t"+num1);
		System.out.println("Second Number :           \t"+num2);

		System.out.println("Addition of Two Numbers :\t"+result);

		result = num1-num2;
		System.out.println("Substraction of Two Numbers :\t"+result);

		result = num1*num2;
		System.out.println("Multiplication of Two Numbers :\t"+result);

		result = num1/num2;
		System.out.println("Divide of Two Numbers :      \t"+result);

		System.out.println("Program Ends here");
		
	}

}